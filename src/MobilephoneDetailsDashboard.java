    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
        // TODO add your handling code here:
       Mobilephone md = new Mobilephone() {}; //create object for mobile phone account
       
       int registerid = Integer.parseInt(registerid_txt.getText());
       String phonename  = phonename_txt.getText();
       String brand = brand_txt.getText();
       String category = category_txt.getText();
       int price = Integer.parseInt(price_txt.getText());
        
       md.setRegisterid(registerid);
       md.setPhonename( phonename);
       md.setBrand(brand);
       md.setCategory(category);
       md.setPrice(price);
       
       ManagerAccount mdi = new ManagerAccount(); // create object for manager account
       mdi.Add(md);
      
       registerid_txt.setText("");
       phonename_txt.setText("");
       brand_txt.setText("");
       category_txt.setText("");
       price_txt.setText("");
        
        
    }//GEN-LAST:event_add_btnActionPerformed
