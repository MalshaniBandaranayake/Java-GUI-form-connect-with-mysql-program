    private void view_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_view_btnActionPerformed
        // TODO add your handling code here:
        ManagerAccount mdi = new ManagerAccount(); // use above creating object for manager account
        List<Mobilephone> list = mdi.viewAll();
        DefaultTableModel dtm = (DefaultTableModel)updatetable_tbl.getModel(); // create object for default table and it set to get values
        dtm.setRowCount(0);
        for (Mobilephone mp: list){
            
            int registraterid = mp.getRegisterid();
            String phonename  = mp.getPhonename();
            String brand      = mp.getBrand();
            String category   = mp.getCategory();
            int price         = mp.getPrice();
            dtm.addRow(new Object[]{ registraterid,phonename,brand,category,price}); // set view details in jtable
           
        
        }
    }//GEN-LAST:event_view_btnActionPerformed
